def update_location_component(location_name, villages, component_path):
    """Update a location component file with nearby villages."""
    if not os.path.exists(component_path):
        print(f"File not found: {component_path}")
        return False
    
    with open(component_path, 'r') as f:
        content = f.read()
    
    # Find the nearbyTowns array and replace it
    # Pattern to match the nearbyTowns array
    pattern = r'const nearbyTowns = \[([^\]]*)\];'
    
    # Create new villages array
    villages_str = ', '.join([f'"{v}"' for v in villages])
    new_towns = f'const nearbyTowns = [{villages_str}];'
    
    # Replace in content
    new_content = re.sub(pattern, new_towns, content)
    
    if new_content != content:
        with open(component_path, 'w') as f:
            f.write(new_content)
        print(f"Updated: {component_path}")
        return True
    else:
        print(f"No changes needed: {component_path}")
        return False

