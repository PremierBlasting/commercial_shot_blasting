def get_nearby_villages(location_name, location_coords, max_distance_km=30, max_villages=15):
    """Find villages within max_distance_km of the given location."""
    lat1, lon1 = location_coords
    nearby = []
    
    for village_name, (lat2, lon2) in uk_villages.items():
        # Skip if it's the same location
        if village_name.lower() == location_name.lower():
            continue
        
        distance = haversine(lat1, lon1, lat2, lon2)
        if distance <= max_distance_km and distance > 0:
            nearby.append((village_name, distance))
    
    # Sort by distance and take top max_villages
    nearby.sort(key=lambda x: x[1])
    return [name for name, _ in nearby[:max_villages]]

